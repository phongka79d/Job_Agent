"""
SQLAlchemy models base and storage conventions.

This module establishes the declarative base and storage conventions
for the Agentic Job Matching System database models, complying with
SQLAlchemy 2.x conventions and SQLite storage guidelines.
"""

from datetime import datetime, timezone
import uuid
from typing import Annotated

from sqlalchemy import Boolean, DateTime, Float, ForeignKey, Integer, String, Text, Index, text
from sqlalchemy.orm import DeclarativeBase, Mapped, mapped_column


class Base(DeclarativeBase):
    """
    SQLAlchemy Declarative Base using SQLAlchemy 2.x conventions.
    All database models will inherit from this base class.
    """
    pass


def generate_uuid() -> str:
    """
    Generate a canonical UUID4 string.
    
    Used as the default generator for UUID-based string primary keys.
    """
    return str(uuid.uuid4())


def utc_now() -> datetime:
    """
    Generate a UTC datetime object with timezone offset.
    
    Used as the default generator for consistently stored timestamps.
    """
    return datetime.now(timezone.utc)


# Storage Conventions (Reusable Annotations for model columns)

# UUID Primary Key: TEXT canonical UUID string, autogenerated by default
uuid_pk = Annotated[
    str,
    mapped_column(
        String(36),
        primary_key=True,
        default=generate_uuid,
        doc="Canonical UUID string primary key"
    )
]

# Timestamps: Consistent SQLAlchemy DateTime stored as timezone-aware UTC datetime.
created_timestamp = Annotated[
    datetime,
    mapped_column(
        DateTime(timezone=True),
        default=utc_now,
        nullable=False,
        doc="Record creation timestamp (UTC)"
    )
]

updated_timestamp = Annotated[
    datetime,
    mapped_column(
        DateTime(timezone=True),
        default=utc_now,
        onupdate=utc_now,
        nullable=False,
        doc="Record update timestamp (UTC)"
    )
]


