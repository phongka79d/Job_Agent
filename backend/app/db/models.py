class Application(Base):
    """
    Application ORM model representing job application status and details.

    Explicit Delete Behavior:
    We use ON DELETE CASCADE on the job_post_id foreign key. When a job post is deleted,
    SQLite will automatically delete any matching application tracking records, ensuring
    database consistency and safety.

    Allowed Statuses:
    Must align with app.core.constants.APPLICATION_STATUSES:
    'applied', 'interview', 'rejected', 'offer'.
    """
    __tablename__ = "applications"

    __table_args__ = (
        Index("idx_applications_job_post_id", "job_post_id"),
    )

    id: Mapped[uuid_pk]
    job_post_id: Mapped[str] = mapped_column(
        String(36),
        ForeignKey("job_posts.id", ondelete="CASCADE"),
        nullable=False,
        doc="Job Post ID as UUID string (Foreign Key with explicit ON DELETE CASCADE)"
    )
    status: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        doc="Application status (applied/interview/rejected/offer)"
    )
    cv_version: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        doc="CV version used for the application"
    )
    notes: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        doc="Notes/details about the application"
    )
    applied_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
        doc="Application timestamp (UTC)"
    )
    updated_at: Mapped[updated_timestamp]


